package com.javase.string;

public class DoubleQuoteMark {
	public static void main(String[] args) {
		String s01 = "\121\"";
		String s02 = "'12121'";
		System.out.println(s01);
		System.out.println(s02);

		String s03 = "\'";
		System.out.println(s03);
		String s04 = "\\";
		System.out.println(s04);
	}

}
